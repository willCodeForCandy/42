#include <unistd.h>

void	write_number(char *num)
{
	while (!(*num > 'a' && *num < 'z') && !(*num > 'A' && *num < 'Z'))
		num++;
	while (*num != '\n')
	{
		write(1, num, 1);
		num++;
	}
	write(1, "\n", 1);
}
