#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		pos;
	char	*ptr;

	pos = 0;
	ptr = (char *)s;
	while (n)
	{
		ptr[pos] = 0;
		pos++;
		n--;
	}
}
