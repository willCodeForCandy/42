int	is_valid(int argc, char *input, int clues[])
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (argc != 2)
		return (0);
	while (input[i] != '\0')
	{
		if (input[i] >= '1' && input[i] <= '4')
		{
			clues[j] = input[i] - '0';
			j++;
		}
		i++;
	}
	if (j != 16)
		return (0);
	return (1);
}
